package com.yfc.valueBean;

public class LinkBean {
	private int linkId;
	private String linkLoveURL;
	private String linkName;
	public int getLinkId() {
		return linkId;
	}
	public void setLinkId(int linkId) {
		this.linkId = linkId;
	}
	public String getLinkLoveURL() {
		return linkLoveURL;
	}
	public void setLinkLoveURL(String linkLoveURL) {
		this.linkLoveURL = linkLoveURL;
	}
	public String getLinkName() {
		return linkName;
	}
	public void setLinkName(String linkName) {
		this.linkName = linkName;
	}
	
	
}
